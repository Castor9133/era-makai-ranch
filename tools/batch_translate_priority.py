def apply_translations():
    """按行替换 MESSAGE_BOX 内容"""
    for (filename, lineno), new_text in TRANSLATIONS.items():
        filepath = KOJO / filename
        if not filepath.exists():
            print(f"[ERR] 文件不存在: {filename}")
            continue

        text = filepath.read_text(encoding='utf-8-sig')
        lines = text.split('\n')

        if lineno > len(lines):
            print(f"[ERR] {filename}:L{lineno} 超出范围(共{len(lines)}行)")
            continue

        old_line = lines[lineno - 1]

        # 验证行包含MESSAGE_BOX
        if 'MESSAGE_BOX' not in old_line:
            print(f"[ERR] {filename}:L{lineno} 不是MESSAGE_BOX行")
            continue

        # 重建新行：替换 @"..." 中的内容
        idx = old_line.find('@"')
        if idx < 0:
            print(f"[ERR] {filename}:L{lineno} 找不到 @\"")
            continue

        rest = old_line[idx+2:]
        last_quote = rest.rfind('"')
        if last_quote < 0:
            print(f"[ERR] {filename}:L{lineno} 找不到结束引号")
            continue

        # 新行 = 前面部分 + @"新内容" + 后面部分
        prefix = old_line[:idx+2]
        suffix = rest[last_quote:]  # 包含右引号
        new_line = prefix + new_text + suffix

        lines[lineno - 1] = new_line

        # 写回文件
        filepath.write_text('\n'.join(lines), encoding='utf-8')
        print(f"[OK] {filename}:L{lineno}")

    print(f"\n总计翻译: {len(TRANSLATIONS)} 行")

if __name__ == '__main__':
    apply_translations()
