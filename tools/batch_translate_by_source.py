def apply_translations():
    """用源文替换方式应用翻译"""
    for (filename, source_jp), target_cn in TRANSLATIONS.items():
        filepath = KOJO / filename
        if not filepath.exists():
            print(f"[ERR] 文件不存在: {filename}")
            continue

        text = filepath.read_text(encoding='utf-8-sig')

        # 构建完整的MESSAGE_BOX字符串
        old_str = f'CALL MESSAGE_BOX,@"{source_jp}"'
        new_str = f'CALL MESSAGE_BOX,@"{target_cn}"'

        if old_str not in text:
            print(f"[MISS] {filename}: 找不到原文片段: {source_jp[:50]}...")
            continue

        count = text.count(old_str)
        text = text.replace(old_str, new_str)

        filepath.write_text(text, encoding='utf-8')
        print(f"[OK] {filename}: 替换{count}处: {source_jp[:40]}...")

    print(f"\n总计翻译: {len(TRANSLATIONS)} 条目")

if __name__ == '__main__':
    apply_translations()
