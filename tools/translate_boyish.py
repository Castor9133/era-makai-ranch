def apply():
    text = FPATH.read_text(encoding='utf-8-sig')
    count = 0
    missed = []
    for source, target in T.items():
        src_norm = source
        tgt_norm = target
        # Handle literal \n in ERB files
        if '\n' in source and '\\n' not in repr(source):
            src_norm = source.replace('\n', chr(92) + 'n')
            tgt_norm = target.replace('\n', chr(92) + 'n')
        old_str = f'CALL MESSAGE_BOX,@\"{src_norm}\"'
        new_str = f'CALL MESSAGE_BOX,@\"{tgt_norm}\"'
        if old_str not in text:
            missed.append(src_norm[:60])
            continue
        n = text.count(old_str)
        text = text.replace(old_str, new_str)
        count += n

    FPATH.write_text(text, encoding='utf-8')
    print(f'OK: {count} replacements')
    if missed:
        print(f'MISSED ({len(missed)}):')
        for m in missed[:20]:
            print(f'  {m}')
        if len(missed) > 20:
            print(f'  ... and {len(missed)-20} more')

if __name__ == '__main__':
    apply()
