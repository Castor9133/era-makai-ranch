# 分类组合（先精确短语，再拟声词，最后单字）
def build_regex_dict():
    """构建 (pattern, replacement) 列表，按优先级排序"""
    items = []

    # 1. 精确短语（原文→译文，保持原文大小写敏感）
    for src, dst in PHRASE_DICT:
        items.append((re.escape(src), dst))

    # 2. 日文标点符号
    items.extend([
        ("。", "。"),
        ("、", "、"),
        ("「", ""),
        ("」", ""),
        ("『", ""),
        ("』", ""),
    ])

    return items


def has_kana(text):
    """检测文本中是否包含日文假名"""
    kana_pattern = re.compile(r'[\u3040-\u309F\u30A0-\u30FF]')
    return bool(kana_pattern.search(text))


def translate_line(text):
    """翻译一行 MESSAGE_BOX 内容"""
    if not has_kana(text):
        return text

    original = text

    # 1. 先替换长短语
    for pattern, replacement in build_regex_dict():
        text = re.sub(pattern, replacement, text)

    # 2. 再替换拟声词组合
    # 从长到短排序，避免短词先匹配
    sorted_onomatopoeia = sorted(ONOMATOPOEIA_MAP.items(), key=lambda x: -len(x[0]))
    for src, dst in sorted_onomatopoeia:
        text = text.replace(src, dst)

    # 3. 最后替换单字假名
    # 对于仍然有假名的行，逐个替换
    if has_kana(text):
        # 对仍然有假名残留的，尝试用 KANA_MAP
        # 使用正则逐个替换，避免破坏已有译文
        for kana, cn in sorted(KANA_MAP.items(), key=lambda x: -len(x[0])):
            if kana in text:
                text = text.replace(kana, cn)

    return text


def process_file(filepath):
    """处理单个 ERB 文件"""
    with open(filepath, "r", encoding="utf-8-sig") as f:
        content = f.read()

    lines = content.split("\n")
    modified = False
    replaced_count = 0

    mb_pattern = re.compile(r'(CALL MESSAGE_BOX,\s*@")(.*?)("\))')

    for i, line in enumerate(lines):
        # 跳过注释行
        if line.strip().startswith(";") or line.strip().startswith("#"):
            continue

        # 查找 MESSAGE_BOX 行
        m = mb_pattern.search(line)
        if m and has_kana(m.group(2)):
            prefix = m.group(1)
            body = m.group(2)
            suffix = m.group(3)

            translated = translate_line(body)
            if translated != body:
                lines[i] = line.replace(body, translated)
                modified = True
                replaced_count += 1
                print(f"  L{i+1}: {body[:40]}... → {translated[:40]}...")

    if modified:
        with open(filepath, "w", encoding="utf-8") as f:
            f.write("\n".join(lines))
        print(f"[OK] {os.path.basename(filepath)}: 替换 {replaced_count} 行")
    else:
        print(f"[--] {os.path.basename(filepath)}: 无改动")

    return replaced_count


def main():
    base_dir = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
                            "ERB", "○口上", "汎用口上")

    if not os.path.exists(base_dir):
        print(f"[ERROR] 目录不存在: {base_dir}")
        sys.exit(1)

    # 获取所有 .ERB 和 .txt 文件
    files = [f for f in os.listdir(base_dir)
             if f.endswith((".ERB", ".txt")) and not f.endswith(".bak")]

    total = 0

    for fname in sorted(files):
        fpath = os.path.join(base_dir, fname)
        print(f"\n{'='*60}")
        print(f"处理: {fname}")
        total += process_file(fpath)

    print(f"\n{'='*60}")
    print(f"全部完成！共替换 {total} 行")


if __name__ == "__main__":
    main()
