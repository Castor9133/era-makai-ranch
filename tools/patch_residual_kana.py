def has_kana(t):
    return bool(re.search(r'[\u3040-\u309F\u30A0-\u30FF]', t))

def process_file(fpath):
    with open(fpath, 'r', encoding='utf-8-sig') as f:
        content = f.read()
    lines = content.split('\n')
    changed = False
    file_count = 0

    for i, line in enumerate(lines):
        if 'MESSAGE_BOX' not in line or '@"' not in line:
            continue
        if 'test_' in line:
            continue
        # 提取 @" 到行尾最后一个 " 之间的内容
        idx = line.find('@"')
        if idx < 0:
            continue
        rest = line[idx+2:]  # after @"
        # 找到最后一个 "
        last_quote = rest.rfind('"')
        if last_quote < 0:
            continue
        inner = rest[:last_quote]
        if not has_kana(inner):
            continue

        new_inner = inner
        # 只做精确短语替换，不做单字替换（单字替换会破坏混合句）
        for old, new in sorted(PATCHES.items(), key=lambda x: -len(x[0])):
            new_inner = new_inner.replace(old, new)

        if new_inner != inner:
            lines[i] = line[:idx+2] + new_inner + rest[last_quote:]
            file_count += 1
            changed = True

    if changed:
        with open(fpath, 'w', encoding='utf-8') as f:
            f.write('\n'.join(lines))
    return file_count

def main():
    files = sorted(f for f in os.listdir(ROOT) if f.endswith(('.ERB','.txt')) and not f.endswith('.bak'))
    total = 0
    for fname in files:
        fpath = os.path.join(ROOT, fname)
        n = process_file(fpath)
        if n > 0:
            total += n
            print(f"[OK] {fname}: {n} 处")
    print(f"\n总计: {total} 处替换")

if __name__ == "__main__":
    main()
